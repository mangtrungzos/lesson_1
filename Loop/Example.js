// Example loop
// In ra 1 dãy số từ 100 về 1

for (var i = 100; i > 0; i--){
    console.log(i);
}

// In ra các giá 0 5 10 15 (Tức là cộng 5 các giá trị mỗi lần)
for (var i = 0; i <= 100; i += 5){
    console.log(i);
}

// In ra các giá trị từ 100 tới 0 và giảm 5 qua mỗi lần lặp
for (var i = 100; i > 0; i -= 5){
    console.log(i);
}